function countGreaterThanY(arr, y) {
    var count = 0;
    // your code here
    for( var i=0 ; i<arr.length ; i++){
        if(arr[i]>y){
            count++;
        }
    }
    return count;
}

countGreaterThanY([22, 12, 13.6, 19, 42, 8], 15);

